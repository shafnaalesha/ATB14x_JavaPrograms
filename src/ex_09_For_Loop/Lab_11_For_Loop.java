package ex_09_For_Loop;

public class Lab_11_For_Loop {
    public static void main(String[] args) {
        for (int i = -1; i > -10 ; i--) {
            System.out.println(i);
            
        }
        int i =10;
        for (;i > 0; i--) {
            System.out.println(i);
        }
    }
}
