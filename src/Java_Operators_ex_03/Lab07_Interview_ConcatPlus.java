package Java_Operators_ex_03;

public class Lab07_Interview_ConcatPlus {
    public static void main(String[] args) {
        // + ->
        int a = 10;
        int b = 20;
        System.out.println(a+b);

        //=-> + String-> Concatenation
        String first_name = "Pramod";
        String second_name = "Dutta";
        System.out.println(first_name+second_name);
//+ behave differently with data type

    }
}
